create table if not exists public.moderation_flags (
  id uuid primary key default gen_random_uuid(),
  entity_type text not null check (entity_type in ('post', 'comment', 'post_reaction', 'comment_reaction', 'profile')),
  entity_id text not null,
  related_post_id uuid null references public.posts(id) on delete set null,
  related_comment_id uuid null references public.post_comments(id) on delete set null,
  related_profile_id uuid null references public.profiles(id) on delete set null,
  actor_user_id uuid null references public.profiles(id) on delete set null,
  content_url text not null,
  excerpt text null,
  reason text not null,
  categories text[] not null default '{}'::text[],
  confidence_score numeric(4, 3) not null check (confidence_score >= 0 and confidence_score <= 1),
  source_created_at timestamptz not null,
  metadata jsonb not null default '{}'::jsonb,
  status text not null default 'open' check (status in ('open', 'reviewed', 'dismissed')),
  last_seen_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  reviewed_at timestamptz null,
  reviewed_by uuid null references public.profiles(id) on delete set null
);

